public class o6_Increment_decrement {
    public static void main(String[] args){
        int x = 1;
        // to increment by 1 through x += 1;
        // you can use the code for shortcut
        x++;
//        x++; increment by 1 once more and results in 3
        System.out.println(x);
        System.out.println("Now Decrement y from 1 to 0 using 'y--;' :");
        int y = 1;
        y--;
        // y --;
        System.out.println(y);

    }
}
